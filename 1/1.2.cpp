// Завдання2. Створитидвовимірний масивчисел розмірностю m на n елементів та 
// заповнити його за допомогоюгенератора випадковихчисел значеннями в діапазоні [a ... 
// b]. (a, b, m та n обрати з таблиці за варіантом). Отриманий двовимірний масив вивести 
// на екран.


// Варіант a  b   m  n 
// 12      85 800 15 9 
// Операція з отриманим масивом
// Поміняти місцями максимальний і третій елементи.

#include <iostream>
#include <cstdlib>
#include <ctime>

using namespace std;

const int a = 85;
const int b = 800;
const int m = 15;
const int n = 9;

// Функція для генерації випадкового числа в діапазоні [a, b]
int generateRandomNumber() {
    return rand() % (b - a + 1) + a;
}

void coutArray(int array[n][m]){
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            cout << array[i][j] << "  ";
        }
        cout << endl;
    }
}

int main() {
    // Ініціалізація генератора випадкових чисел
    srand(static_cast<unsigned int>(time(0)));

    // Створення та заповнення масиву
    int array[n][m];
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            array[i][j] = generateRandomNumber();
        }
    }

    // Вивід отриманого масиву
    cout << "Отриманий масив:\n";
    coutArray(array);

    // Поміняти місцями максимальний і третій елементи
    int maxElement = array[0][0];
    int maxRow = 0;
    int maxCol = 0;

    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < m; ++j) {
            if (array[i][j] > maxElement) {
                maxElement = array[i][j];
                maxRow = i;
                maxCol = j;
            }
        }
    }

    // Заміна максимального та третього елементів
    int temp = array[2][0];
    array[0][2] = maxElement;
    array[maxRow][maxCol] = temp;

    // Вивід масиву після обміну
    cout << "\nМасив після обміну:\n";
    coutArray(array);

    return 0;
}


